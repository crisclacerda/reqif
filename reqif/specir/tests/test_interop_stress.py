"""Interoperability stress tests: StrictDoc (.sdoc) <-> ReqIF <-> SpecIR <-> CommonSpec.

Three test classes covering two end-to-end pipelines:

  Stress Test 1 — TestSDocToCommonSpec (10 tests):
    Real StrictDoc L2 requirements (.sdoc) -> strictdoc export -> ReqIF
    -> SpecIR import -> decompile -> CommonSpec
    Uses the actual StrictDoc project's high-level requirements (179 UIDs,
    53 sections).  External parent relations (to L1/ZEP/DO178 UIDs) are
    stripped programmatically before export.

  Stress Test 2a — TestEngineeringReqIFRoundTrip (9 tests):
    Engineering DB (SRS) -> export_reqif -> ReqIF -> SpecIR reimport
    -> decompile -> CommonSpec

  Stress Test 2b — TestEngineeringSDocInterop (7 tests):
    Engineering DB (SRS) -> export_reqif -> ReqIF -> strictdoc import
    -> .sdoc validation

Stress Test 1 requires StrictDoc (pip install strictdoc).
Stress Test 2 additionally requires the engineering docs database.

Artifacts are persisted under tests/fixtures/interop/ for inspection:

  sdoc_to_commonspec/
    input/*.sdoc                  StrictDoc L2 .sdoc (external refs stripped)
    reqif/*.reqif                 ReqIF exported by StrictDoc
    commonspec/*.md               CommonSpec decompiled from SpecIR

  engineering_round_trip/
    reqif/srs.reqif               ReqIF exported from engineering DB
    commonspec/*.md               CommonSpec decompiled after round-trip

  engineering_sdoc/
    sdoc/*.sdoc                   .sdoc files generated by StrictDoc from our ReqIF
"""
