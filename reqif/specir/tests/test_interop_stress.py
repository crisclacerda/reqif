def _clean_dir(path):
    """Remove and recreate a directory so artifacts are always fresh."""
    if os.path.exists(path):
        shutil.rmtree(path)
    os.makedirs(path)


# ── StrictDoc availability ────────────────────────────────────────────────

def _has_strictdoc():
    try:
        r = subprocess.run(
            ["strictdoc", "--version"],
            capture_output=True, text=True, timeout=15,
        )
        return r.returncode == 0
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False


_HAVE_STRICTDOC = _has_strictdoc()


# ── StrictDoc CLI wrappers ────────────────────────────────────────────────

def _strictdoc_export(input_dir, output_dir):
    r = subprocess.run(
        ["strictdoc", "export", input_dir,
         "--formats=reqif-sdoc",
         "--output-dir", output_dir,
         "--no-parallelization"],
        capture_output=True, text=True, timeout=120,
    )
    return r.returncode, r.stdout, r.stderr


def _strictdoc_import_reqif(reqif_path, output_dir):
    r = subprocess.run(
        ["strictdoc", "import", "reqif", "p01_sdoc",
         reqif_path, output_dir],
        capture_output=True, text=True, timeout=120,
    )
    return r.returncode, r.stdout, r.stderr


# ── Helpers ───────────────────────────────────────────────────────────────

def _find_reqif_file(directory):
    files = glob.glob(os.path.join(directory, "**", "*.reqif"), recursive=True)
    return files[0] if files else None


def _find_sdoc_files(directory):
    return glob.glob(os.path.join(directory, "**", "*.sdoc"), recursive=True)


def _read_all_md(directory):
    result = {}
    for path in glob.glob(os.path.join(directory, "**", "*.md"), recursive=True):
        with open(path, encoding="utf-8") as f:
            result[os.path.relpath(path, directory)] = f.read()
    return result


def _strip_external_refs(content):
    """Remove RELATIONS entries that reference UIDs outside this document.

    Strips Parent relations to SDOC-SSS-*, ZEP-*, DO178-* and all File
    relations.  Operates only on requirement-level RELATIONS: blocks, not
    the [GRAMMAR] definition.
    """
    lines = content.split("\n")
    out = []
    in_grammar = False
    in_relations = False
    relations_had_entries = False
    relations_start_idx = None

    i = 0
    while i < len(lines):
        line = lines[i]

        # Track grammar block (don't touch GRAMMAR relation type defs)
        if line.strip() == "[GRAMMAR]":
            in_grammar = True
        elif in_grammar and re.match(
            r"^\s*\[(REQUIREMENT|TEXT)\]$|^\s*\[\[SECTION\]\]$", line.strip()
        ):
            in_grammar = False

        if in_grammar:
            out.append(line)
            i += 1
            continue

        # Detect RELATIONS: block
        if line.strip() == "RELATIONS:":
            in_relations = True
            relations_had_entries = False
            relations_start_idx = len(out)
            out.append(line)
            i += 1
            continue

        if in_relations:
            if re.match(r"^- TYPE:\s+", line):
                rel_type = re.match(r"^- TYPE:\s+(\S+)", line).group(1)

                # Skip File relations entirely
                if rel_type == "File":
                    i += 1
                    while i < len(lines) and lines[i].strip().startswith(
                        ("VALUE:", "FORMAT:")
                    ):
                        i += 1
                    continue

                # Skip Parent relations to external UIDs
                if rel_type == "Parent" and i + 1 < len(lines):
                    val_match = re.match(
                        r"\s*VALUE:\s+(\S+)", lines[i + 1]
                    )
                    if val_match and re.match(
                        r"(SDOC-SSS|ZEP|DO178)-", val_match.group(1)
                    ):
                        i += 2
                        continue

                # Keep this relation entry
                relations_had_entries = True
                out.append(line)
                i += 1
                continue
            else:
                # End of RELATIONS block
                in_relations = False
                if not relations_had_entries:
                    del out[relations_start_idx]
                out.append(line)
                i += 1
                continue

        out.append(line)
        i += 1

    return "\n".join(out)


