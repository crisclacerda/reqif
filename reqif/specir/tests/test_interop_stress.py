@unittest.skipUnless(os.path.exists(_ENGINEERING_DB), "engineering docs DB not found")
class TestEngineeringReqIFRoundTrip(unittest.TestCase):
    """ReqIF round-trip with real data: eng DB -> ReqIF -> SpecIR -> CommonSpec.

    Tests that our exporter produces valid ReqIF for a complex real-world spec
    (SRS: 90 objects, 17 types, 119 relations) and that our importer can
    re-consume it faithfully.

    Artifacts persisted to fixtures/interop/engineering_round_trip/.
    """

    @classmethod
    def setUpClass(cls):
        from reqif.unparser import ReqIFUnparser

        base = os.path.join(_FIXTURES_DIR, "engineering_round_trip")

        # Phase A: Export SRS from engineering DB to ReqIF.
        conn_src = sqlite3.connect(_ENGINEERING_DB)
        conn_src.row_factory = sqlite3.Row

        cls._orig_object_count = conn_src.execute(
            "SELECT COUNT(*) FROM spec_objects WHERE specification_ref = 'srs'"
        ).fetchone()[0]
        cls._orig_relation_count = conn_src.execute(
            "SELECT COUNT(*) FROM spec_relations WHERE specification_ref = 'srs'"
        ).fetchone()[0]

        rows = conn_src.execute(
            "SELECT title_text FROM spec_objects "
            "WHERE specification_ref = 'srs' AND type_ref = 'HLR' "
            "ORDER BY file_seq LIMIT 5"
        ).fetchall()
        cls._key_titles = [str(r[0]) for r in rows if r[0]]

        bundle_a = export_reqif(conn_src, "srs")
        conn_src.close()

        xml_a = ReqIFUnparser.unparse(bundle_a)
        reqif_dir = os.path.join(base, "reqif")
        _clean_dir(reqif_dir)
        cls._reqif_path = os.path.join(reqif_dir, "srs.reqif")
        with open(cls._reqif_path, "w", encoding="utf-8") as f:
            f.write(xml_a)
        cls._reqif_size = len(xml_a)

        # Phase B: Re-import the ReqIF into a fresh SpecIR.
        bundle_b = ReqIFParser.parse(cls._reqif_path)
        cls._conn = sqlite3.connect(":memory:")
        cls._spec_id = import_reqif(bundle_b, cls._conn)
        cls._conn.row_factory = sqlite3.Row

        cls._reimport_object_count = cls._conn.execute(
            "SELECT COUNT(*) FROM spec_objects WHERE specification_ref = ?",
            (cls._spec_id,),
        ).fetchone()[0]
        cls._reimport_relation_count = cls._conn.execute(
            "SELECT COUNT(*) FROM spec_relations WHERE specification_ref = ?",
            (cls._spec_id,),
        ).fetchone()[0]

        # Phase C: Decompile to CommonSpec.
        decomp_dir = os.path.join(base, "commonspec")
        _clean_dir(decomp_dir)
        cls._md_files = decompile(
            cls._conn, cls._spec_id, decomp_dir, overwrite=True,
        )
        cls._md_dict = _read_all_md(decomp_dir)
        cls._md = "\n".join(cls._md_dict.values())

    @classmethod
    def tearDownClass(cls):
        cls._conn.close()

    def test_reqif_size(self):
        """Exported ReqIF should be substantial for a 90-object spec."""
        self.assertGreater(self._reqif_size, 100_000,
                           f"ReqIF too small: {self._reqif_size}")

    def test_object_count_preserved(self):
        """All objects should survive the export→import round-trip."""
        self.assertEqual(self._reimport_object_count, self._orig_object_count)

    def test_relation_count_preserved(self):
        """Relations should survive the export→import round-trip.

        The DB may have duplicate relations (same source+target+type) that
        collapse to one in ReqIF, so allow a small delta.
        """
        delta = self._orig_relation_count - self._reimport_relation_count
        self.assertLessEqual(delta, 5,
                             f"Too many relations lost: {delta} "
                             f"(orig={self._orig_relation_count}, "
                             f"reimport={self._reimport_relation_count})")

    def test_key_titles_in_commonspec(self):
        """HLR titles from the SRS should appear in CommonSpec output."""
        found = sum(1 for t in self._key_titles if t in self._md)
        self.assertEqual(found, len(self._key_titles),
                         f"Missing titles: {[t for t in self._key_titles if t not in self._md]}")

    def test_commonspec_has_many_headings(self):
        h2_count = sum(1 for l in self._md.split("\n") if l.startswith("## "))
        self.assertGreaterEqual(h2_count, 10, f"Expected >= 10 H2 headings, got {h2_count}")

    def test_commonspec_has_pids(self):
        pid_matches = re.findall(r"@\S+", self._md)
        self.assertGreater(len(pid_matches), 20, "Expected > 20 @PID markers")

    def test_hierarchical_structure(self):
        has_h2 = any(l.startswith("## ") for l in self._md.split("\n"))
        has_h3 = any(l.startswith("### ") for l in self._md.split("\n"))
        self.assertTrue(has_h2, "No H2 headings")
        self.assertTrue(has_h3, "No H3 headings")

    def test_commonspec_has_attributes(self):
        """CommonSpec should have blockquote attribute lines."""
        attr_lines = [l for l in self._md.split("\n") if l.startswith("> ")]
        self.assertGreater(len(attr_lines), 5, "Expected > 5 attribute lines")

    def test_commonspec_has_relations(self):
        """CommonSpec should have relation links [target](@)."""
        links = re.findall(r"\[[\w-]+\]\(@\)", self._md)
        self.assertGreater(len(links), 0, "No relation links found")


