@unittest.skipUnless(os.path.exists(_ENGINEERING_DB), "engineering docs DB not found")
class TestDoubleRoundTripStability(unittest.TestCase):
    """DB → ReqIF → SpecIR → ReqIF → SpecIR: objects, relations, CommonSpec must be stable."""

    @classmethod
    def setUpClass(cls):
        from reqif.unparser import ReqIFUnparser
        import tempfile

        cls._tmpdir = tempfile.TemporaryDirectory()
        tmp = cls._tmpdir.name

        conn_orig = sqlite3.connect(_ENGINEERING_DB)
        conn_orig.row_factory = sqlite3.Row
        cls._orig_objs = conn_orig.execute(
            "SELECT COUNT(*) FROM spec_objects WHERE specification_ref='srs'"
        ).fetchone()[0]
        cls._orig_rels = conn_orig.execute(
            "SELECT COUNT(*) FROM spec_relations WHERE specification_ref='srs'"
        ).fetchone()[0]

        # Round 1
        b1 = export_reqif(conn_orig, "srs")
        conn_orig.close()
        r1_path = os.path.join(tmp, "r1.reqif")
        with open(r1_path, "w") as f:
            f.write(ReqIFUnparser.unparse(b1))

        conn_r1 = sqlite3.connect(":memory:")
        sid_r1 = import_reqif(ReqIFParser.parse(r1_path), conn_r1)
        conn_r1.row_factory = sqlite3.Row
        cls._r1_objs = conn_r1.execute(
            "SELECT COUNT(*) FROM spec_objects WHERE specification_ref=?", (sid_r1,)
        ).fetchone()[0]
        cls._r1_rels = conn_r1.execute(
            "SELECT COUNT(*) FROM spec_relations WHERE specification_ref=?", (sid_r1,)
        ).fetchone()[0]

        # Round 2
        b2 = export_reqif(conn_r1, sid_r1)
        conn_r1.close()
        r2_path = os.path.join(tmp, "r2.reqif")
        with open(r2_path, "w") as f:
            f.write(ReqIFUnparser.unparse(b2))

        conn_r2 = sqlite3.connect(":memory:")
        sid_r2 = import_reqif(ReqIFParser.parse(r2_path), conn_r2)
        conn_r2.row_factory = sqlite3.Row
        cls._r2_objs = conn_r2.execute(
            "SELECT COUNT(*) FROM spec_objects WHERE specification_ref=?", (sid_r2,)
        ).fetchone()[0]
        cls._r2_rels = conn_r2.execute(
            "SELECT COUNT(*) FROM spec_relations WHERE specification_ref=?", (sid_r2,)
        ).fetchone()[0]

        # Decompile both rounds
        cs1 = os.path.join(tmp, "cs1")
        cs2 = os.path.join(tmp, "cs2")
        os.makedirs(cs1); os.makedirs(cs2)

        conn_r1b = sqlite3.connect(":memory:")
        sid_r1b = import_reqif(ReqIFParser.parse(r1_path), conn_r1b)
        decompile(conn_r1b, sid_r1b, cs1, overwrite=True)
        conn_r1b.close()

        decompile(conn_r2, sid_r2, cs2, overwrite=True)
        conn_r2.close()

        md1 = _read_all_md(cs1)
        md2 = _read_all_md(cs2)
        cls._md_r1 = "\n".join(md1[k] for k in sorted(md1))
        cls._md_r2 = "\n".join(md2[k] for k in sorted(md2))

    @classmethod
    def tearDownClass(cls):
        cls._tmpdir.cleanup()

    def test_object_count_stable_round1(self):
        self.assertEqual(self._r1_objs, self._orig_objs)

    def test_object_count_stable_round2(self):
        self.assertEqual(self._r2_objs, self._r1_objs)

    def test_relation_count_stable(self):
        self.assertEqual(self._r2_rels, self._r1_rels)

    def test_commonspec_identical(self):
        """CommonSpec output must be byte-identical after double round-trip."""
        self.assertEqual(self._md_r1, self._md_r2)


